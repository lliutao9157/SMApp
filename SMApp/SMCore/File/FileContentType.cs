     {".z", new Mime{Mimestr="application/x-compress",compress=false } },
     {".zip", new Mime{Mimestr="application/x-zip-compressed",compress=false } },
     {".json", new Mime{Mimestr="application/json",compress=true } },
     #endregion
  
     };

        public static Mime GetMimeType(string extension)
        {
            if (extension == null)
            {
                throw new ArgumentNullException("extension");
            }

            if (!extension.StartsWith("."))
            {
                extension = "." + extension;
            }

            Mime mime;
            return _mappings.TryGetValue(extension, out mime) ? mime : new Mime { Mimestr = "application/octet-stream", compress = false };
        }
    }
    public class Mime
    {
        public string Mimestr { get; set; }
        public bool compress { get; set; }
    }
